package com.cg.operators;

public class UnaryOperator {

	
			public static void main(String[] args) {
				int a=5;
				int res=a++;
				int res2=++a;
				int res3=a--;
				int res4=--a;
				
				System.out.println(res);
				System.out.println(res2);
				System.out.println(res3);
				System.out.println(res4);

			}

		}
	
